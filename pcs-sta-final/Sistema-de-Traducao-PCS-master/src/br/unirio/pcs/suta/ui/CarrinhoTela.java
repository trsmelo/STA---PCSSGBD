    /**
     * Mostra a soma dos itens no carrinho.
     */
    public void mostrarTotal() {
        ObraDAO ob = new ObraDAO();
        double total = 0;
        double total_arr = 0;
        try {
            this.obras = ob.listarObras();
            for (Obra obra : obras) {

                total += obra.getPreco();
            }

            total_arr = Math.round(total * 100.0) / 100.0;
            String total_convertido = String.valueOf(total_arr);
            this.totalCompra.setText(total_convertido);

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

