    /**
     *
     * @throws Exception
     */
    private void listObraEspecifica() throws Exception { // TODO: Passar um nome especifico de uma obra para ser adicionada.

        ObraDAO ob = new ObraDAO();
        this.obras = ob.listarObras();
        DefaultTableModel model = (DefaultTableModel) visuPedidoTabela.getModel();

        for (int i = 0; i < obras.size(); i++) {

            Object rowData[] = new Object[3];
            rowData[0] = obras.get(i).getNome();
            rowData[1] = obras.get(i).getAutor();
            rowData[2] = obras.get(i).getPreco();

            model.addRow(rowData);
        }
    }

